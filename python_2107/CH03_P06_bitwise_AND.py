
# bitwise and  => &

a = 7
b = 3
""" 
    __doc__ string

    7 : 0111
    3 : 0011
    ----------
    & : 0011 = > 3
"""
c = a&b
print(a," & ",b," = ",c)

# bitwise or => |
a = 7
b = 3
""" 
    __doc__ string

    7 : 0111
    3 : 0011
    ----------
    | : 0111 => 7
"""
c = a|b
print(a," | ",b," = ",c)
