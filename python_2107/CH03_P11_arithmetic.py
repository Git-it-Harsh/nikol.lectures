
# arithmetic operations

"""
    + : addition
    - : substration
    * : multiplication
    / : division
    % : modulo
   ** : exponetial
"""

x = int(input("Enter the number : "))
y = int(input("Enter the number : "))

print(x," + ",y," = ",x+y)
print(x," - ",y," = ",x-y)
print(x," * ",y," = ",x*y)
print(x," / ",y," = ",x/y)
print(x," % ",y," = ",x%y)
print(x," ** ",y," = ",x**y)