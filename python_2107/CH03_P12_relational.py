
# relational
"""
    > : greater than
    < : less than
    >=: greater than or equals
    <=: less than or equals
    ==: equals to [ check same value]
    !=: not equals to [check not same.]
"""

x = int(input("Enter the number "))
y = int(input("Enter the number "))

print(x,">",y,"=",x>y)
print(x,"<",y,"=",x<y)
print(x,">=",y,"=",x>=y)
print(x,"<=",y,"=",x<=y)
print(x,"==",y,"=",x==y)
print(x,"!=",y,"=",x!=y)