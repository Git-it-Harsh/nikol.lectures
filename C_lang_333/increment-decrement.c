#include<stdio.h>

int main(){
	
	int num = 30;
	
	
	printf("The value of NUm is %i",num);
	
	num++;   //post increment
	
	printf("\nUpdated Value %d",num);
	
	++num;   //pre incre
	
	printf("\nUpdated Value %d",num);
	
	
	num--;  //post
	
	printf("\nUpdated(decremented) Value %d",num);
	
	--num;     //pre
	
	printf("\nUpdated(decremented) Value %d",num);
	
	
	
	
	return 0;
}
