#include<stdio.h>

int main(){
	
	
	int num = 50;
	
	// 35 == 90
	
	printf("%d",num);
	printf("Equal %d",num == 90);
	
	
	printf("\nNot Equal %d",num != 80);
	
	
	printf("\n Greater than  %d",num > 50);
	
	printf("\n Smaller than  %d",num < 50);
	
	printf("\n Greater than equal %d",num >= 50);
	
	printf("\n Smaller than equal %d",num <= 50);
	return 0;
}
