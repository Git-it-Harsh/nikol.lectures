#include<stdio.h>

int main(){
	
	int num = 4;
	
	
	switch(num){
		
		case 40:{
			printf("The value is 40");
			break;
		}
		
		case 45:{
			printf("The value is 45");
			break;
		}
		
		case 50:{
			printf("The value is 50");
			break;
		}
		default:{
			
			printf("The value is something else ");
			break;
		}
	}
	
	
	return 0;
}
