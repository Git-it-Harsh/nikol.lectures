#include<stdio.h>

int main(){
	
	int num , num1;
	
	
	num = 20;
	num1 = 40;
	
	
	
	printf(" Equal to %d\n",num == num1);
	
	printf(" Not Equal to %d\n",num != num1);
	
	printf(" less than to %d\n",num < num1);
	
	printf(" Greater than to %d\n",num > num1);
	
	printf(" less than equal to %d\n",num <= num1);
	
	printf(" greater than equal to %d\n",num >= num1);
	
	
	return 0;
}
